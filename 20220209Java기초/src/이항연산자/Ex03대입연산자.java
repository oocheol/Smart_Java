package 이항연산자;

public class Ex03대입연산자 {

	public static void main(String[] args) {
		
		// 1. 복합대입연산자
		int num = 0;
		
		num = num + 2;
		System.out.println(num); // 2
		num += 2; // num = num + 2
		System.out.println(num); // 4
		num += 2;
		System.out.println(num); // 6
		num -= 3; // num = num - 3
		System.out.println(num); // 3
		num *= 4;
		System.out.println(num); // 12
		num /= 3;
		System.out.println(num); // 4
		
	
		
	}

}
