package 이항연산자;

public class Ex04비교연산자 {

	public static void main(String[] args) {
		
		// 비교연산자
		int num1 = 4;
		int num2 = 5;
		
		System.out.println(num1 >= num2); // false
		System.out.println(num1 == num2); // false
		// 다르다
		System.out.println(num1 != num2); // true
		
		// 비교연산자의 결과값은 boolean형태로 나온다!!!
		
		
		
		
	}

}
