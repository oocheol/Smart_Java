package 단항연산자;

public class Ex01증감연산자 {

	public static void main(String[] args) {
		
		// 증감연산자
		int num = 1;
		System.out.println(++num); //2
		System.out.println(num); //2
		System.out.println(num++); //2
		System.out.println(num); //3
		
		System.out.println(--num); //2
		System.out.println(num); //2
		System.out.println(num--); //2
		System.out.println(num); //1
		
		
		
		
	}

}
